    /**verifica se os campos estão preenchidos corretamente */
    private Boolean valida(){
        int i;
        if(!((
            jList2.isSelectionEmpty()&&
            jList1.isSelectionEmpty()&&
            main.GroupButtonUtils.getSelectedButtonText(buttonGroup3)==null&&
            main.GroupButtonUtils.getSelectedButtonText(buttonGroup2)==null))){
            try{
                i = Integer.parseInt(jTextField2.getText());
            }catch(NumberFormatException exception){
                JOptionPane.showMessageDialog(null, "Por favor digite um número inteiro");
                jTextField2.setText(null);
                return false;
            }
            return true;
        }else{
            JOptionPane.showMessageDialog(null, "Por favor selecione ao menos um dos filtros");
        }
        return false;
    }
    
    /**
     * Funçao que inicializa a lista de assuntos ja existentes
     */
    public void setassunto(){
        DefaultListModel list = new DefaultListModel();
        try{
            File arq = new File("Indices/Assuntos.txt");
            if (!arq.exists()){
                JOptionPane.showMessageDialog(null, "Nenhum arquivo no banco de dados ou erro desconhecido com as permissões da pasta do programa!(Erro 11)");
                return;
            }
            Scanner fileIn;
            fileIn = new Scanner(arq);
            while (fileIn.hasNextLine()){
                list.addElement(fileIn.nextLine());
            }
            fileIn.close();
            jList1.setModel(list);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Busca.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
