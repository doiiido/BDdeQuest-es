    /**Código do botão Ok*/
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String Assunto;
        String Lingua;
        if(valida()){
            if(!jList1.isSelectionEmpty()){
                Assunto =  jList1.getSelectedValue();
            }else{
                Assunto = "";
            }
            if(!jList2.isSelectionEmpty()){
                Lingua =  jList2.getSelectedValue();
            }else{
                Lingua = "";
            }
            String Tipo = main.GroupButtonUtils.getSelectedButtonText(buttonGroup2);
            if (Tipo==null){
                Tipo="";
            }
            String Dificuldade = main.GroupButtonUtils.getSelectedButtonText(buttonGroup3);
            if (Dificuldade==null){
                Dificuldade="";
            }
            int numero = Integer.parseInt(jTextField2.getText());
            if(numero>0){
                String Resultado = Banco.Busca.buscar(Assunto, Lingua, Tipo, Dificuldade, numero);
                if(Resultado==null)
                    return;
                if(encontradas!=numero){
                    JOptionPane.showMessageDialog(null, "Foram encontradas apenas "+encontradas+" questões");
                }else{
                    JOptionPane.showMessageDialog(null, "Foram encontradas "+numero+" questões");            
                }
                    int check = Banco.ResultadoBusca.ImprimeResultado(Resultado.split(System.lineSeparator()));
                    String file="Buscas/Busca.txt";
                    Scanner fileIn = new Scanner(file);
                    if (check != 0)
                        return;
                try {
                    openfile(file);
                } catch (IOException ex) {
                    Logger.getLogger(Busca.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Erro nas permissões da pasta do programa!(Erro 10)");
                }
            }
            encontradas = 0;
        }
    }//GEN-LAST:event_jButton1MouseClicked

