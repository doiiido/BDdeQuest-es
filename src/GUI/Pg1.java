    /**Código do menu Abrir*/
    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        File dir = new File ("Questoes/");
        fileChooser.setCurrentDirectory(dir);
        int returnVal = fileChooser.showOpenDialog(this);
    if (returnVal == JFileChooser.APPROVE_OPTION) {
           try {
               jButton2ActionPerformed(evt);
               File file = fileChooser.getSelectedFile();
               nome = file.getName();
               try (Scanner fileIn = new Scanner(file)) {
                   String lingua = fileIn.nextLine();
                   jTextField2.setText(lingua);
                   String Assunto = fileIn.nextLine();
                   jTextField1.setText(Assunto);
                   String Tipo = fileIn.nextLine();
                   if(Tipo.equals("Objetiva")){
                       buttonGroup2.setSelected(jRadioButton6.getModel(), rootPaneCheckingEnabled);
                   }
                   if(Tipo.equals("Discursiva")){
                       buttonGroup2.setSelected(jRadioButton7.getModel(), rootPaneCheckingEnabled);
                   }
                   if(Tipo.equals("Match")){
                       buttonGroup2.setSelected(jRadioButton8.getModel(), rootPaneCheckingEnabled);
                   }
                   String Dificuldade = fileIn.nextLine();
                   if(Dificuldade.equals("Fácil")){
                       buttonGroup1.setSelected(jRadioButton1.getModel(), rootPaneCheckingEnabled);
                   }
                   if(Dificuldade.equals("Média")){
                       buttonGroup1.setSelected(jRadioButton2.getModel(), rootPaneCheckingEnabled);
                   }
                   if(Dificuldade.equals("Difícil")){
                       buttonGroup1.setSelected(jRadioButton3.getModel(), rootPaneCheckingEnabled);
                   }
                   fileIn.nextLine();
                   while(fileIn.hasNextLine()){
                       String Texto = fileIn.nextLine();
                       jTextArea1.append(Texto+System.lineSeparator());
                   }
               }
               Edit = true;
               jButton1.setText("Editar");
               jButton2.setText("Cancelar");
           } catch (FileNotFoundException ex) {
               Logger.getLogger(Pg1.class.getName()).log(Level.SEVERE, null, ex);
               JOptionPane.showMessageDialog(null, "Erro nas permissões da pasta do programa!(Erro 9)");
           }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

