    /**Deleta pastas, usada para remover as pastas de indices ao refazer o banco
     * @param dir
     * @return  */
    public static boolean DeletaPastas(File dir){
           if (dir.isDirectory()) {  
               String[] children = dir.list();  
               for (String children1 : children) {
                   boolean success = DeletaPastas(new File(dir, children1));
                   if (!success) {
                       return false;
                   }
               }  
           }  
         
           // Agora o diretório está vazio, restando apenas deletá-lo.  
           return dir.delete();  
       }  
     
    /**verifica se todos campos estão preenchidos corretamente */
    private Boolean valida(){
        String Assunto =  jTextField1.getText();
        String Lingua =  jTextField2.getText();
        if((!Lingua.contains("\\"))&&
            (!Lingua.contains("/"))&&
            (!Lingua.contains("\""))&&
            (!Lingua.contains("*"))&&
            (!Lingua.contains("?"))&&
            (!Lingua.contains(":"))&&
            (!Lingua.contains("<"))&&
            (!Lingua.contains(">"))&&
            (!Lingua.contains("|"))){
            if((!Assunto.contains("\\"))&&
            (!Assunto.contains("/"))&&
            (!Assunto.contains("\""))&&
            (!Assunto.contains("*"))&&
            (!Assunto.contains("?"))&&
            (!Assunto.contains(":"))&&
            (!Assunto.contains("<"))&&
            (!Assunto.contains(">"))&&
            (!Assunto.contains("|"))){
                if(!(jTextField1.getText().isEmpty()||
                    jTextArea1.getText().isEmpty()||
                    jTextField2.getText().isEmpty()||
                    main.GroupButtonUtils.getSelectedButtonText(buttonGroup2)==null||
                    main.GroupButtonUtils.getSelectedButtonText(buttonGroup1)==null)){
                        return true;
                }else{
                    JOptionPane.showMessageDialog(null, "Por favor complete todos os campos");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Por favor não utilize \\, /, |, \", :, *, ?, < ou > no campo Assunto");
                jTextField1.setText(null);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por favor não utilize \\, /, |, \", :, *, ?, < ou > no campo Língua");
                
            jTextField2.setText(null);
        }
        return false;
    }
    
