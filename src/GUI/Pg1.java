    public void setassunto(){
        String Assunto;
       ActionListener menuListener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent event) {
                jTextField1.setText(event.getActionCommand());
              }
        };
        try{
            File arq = new File("Indices/Assuntos.txt");
            Scanner fileIn;
            fileIn = new Scanner(arq);
            while (fileIn.hasNextLine()){
                Assunto=fileIn.nextLine();
                JMenuItem item = new JMenuItem(Assunto);
                item.addActionListener(menuListener);
                jPopupMenu1.add(item);
            }
            fileIn.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Busca.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro nas permissões da pasta do programa!(Erro 31)");
        }
    }
    
